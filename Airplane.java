/**
 * Airplane Class.
 */
public class Airplane {
    /**
     * Speed variable.
     */
    private int speed;

    /**
     * The getSpeed function.
     *
     * @return speed speed of airplane.
     */
    public int getSpeed() {
        return this.speed;
    }

    /**
     * The setSpeed function.
     *
     * @param speed sets speed of airplane.
     */
    public void setSpeed(int speed) {
        this.speed = speed;
    }
}
